export const runEscalationCheck = async () => {
  let tickets = await getTickets();
  const now = Date.now();
  let changed = false;
  for (const t of tickets) {
    if (['Resolved', 'Closed', 'Escalated'].includes(t.status)) continue;
    if (t.slaDeadline && now > new Date(t.slaDeadline).getTime()) {
      t.status = 'Escalated';
      t.escalatedAt = new Date().toISOString();
      t.statusHistory.push({ from: t.status, to: 'Escalated', by: 'System (SLA Breach)', at: t.escalatedAt });
      changed = true;
      await fetch(`${API}/tickets/${t.id}`, { method: 'PUT', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify(t) });
    }
  }
  if (changed) tickets = await getTickets();
  return tickets;
};

export const bulkUpdateStatus = async (ids, newStatus, byName) => {
  let tickets = await getTickets();
  for (const id of ids) {
    const t = tickets.find(x => x.id === id);
    if (!t) continue;
    t.status = newStatus;
    t.resolvedAt = newStatus === 'Resolved' ? new Date().toISOString() : t.resolvedAt;
    t.statusHistory = [...(t.statusHistory || []), { from: t.status, to: newStatus, by: byName, at: new Date().toISOString() }];
    await fetch(`${API}/tickets/${t.id}`, { method: 'PUT', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify(t) });
  }
  return await getTickets();
};

// ─── Analytics ──────────────────────────────────────────────────────────────
export const getAnalytics = async () => {
  const tickets = await getTickets();
  const now = new Date();
  const last7Days = Array.from({ length: 7 }, (_, i) => {
    const d = new Date(); d.setDate(now.getDate() - i);
    return d.toISOString().split('T')[0];
  }).reverse();

  const dailySubmissions = last7Days.map(date => ({
    date: date.slice(5),
    count: tickets.filter(t => t.timestamp.startsWith(date)).length,
  }));

  const deptPerf = CATEGORIES.map(cat => {
    const deptTickets = tickets.filter(t => t.category === cat);
    const deptResolved = deptTickets.filter(t => t.status === 'Resolved');
    let avgRes = 0; let avgRat = 0;
    if (deptResolved.length) {
      avgRes = deptResolved.reduce((acc, t) => acc + (new Date(t.resolvedAt) - new Date(t.timestamp)) / 3600000, 0) / deptResolved.length;
      const rated = deptResolved.filter(t => t.rating);
      if (rated.length) avgRat = rated.reduce((acc, t) => acc + t.rating, 0) / rated.length;
    }
    return {
      department: DEPARTMENTS[cat]?.name || cat, total: deptTickets.length,
      resolved: deptResolved.length,
      escalated: deptTickets.filter(t => t.status === 'Escalated').length,
      resolutionRate: deptTickets.length ? Math.round((deptResolved.length / deptTickets.length) * 100) : 0,
      avgResHours: avgRes, avgRating: avgRat,
    };
  }).filter(d => d.total > 0);

  const byPriority = ['Low', 'Medium', 'High'].map(p => ({
    label: p, count: tickets.filter(t => t.priority === p).length,
  }));

  const users = await getUsers();
  const staffPerf = users.filter(u => u.role === 'staff').map(s => {
    const assigned = tickets.filter(t => t.assignedTo === s.id);
    const staffResolved = assigned.filter(t => t.status === 'Resolved');
    return {
      id: s.id, name: s.name, avatar: s.avatar, department: s.department,
      assigned: assigned.length, resolved: staffResolved.length,
      resolutionRate: assigned.length ? Math.round((staffResolved.length / assigned.length) * 100) : 0,
    };
  });

  return { dailySubmissions, deptPerf, byPriority, staffPerf };
};

