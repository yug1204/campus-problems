export const saveTicket = async (ticket) => {
  const submittedAt = new Date().toISOString();
  // id is generated by backend or here. Let's do it here for simplicity or let backend do it. We'll generate a random id.
  const newTicket = {
    ...ticket,
    id: `TKT-${Math.floor(1000 + Math.random() * 9000)}`,
    status: 'Pending',
    timestamp: submittedAt,
    slaDeadline: makeSla(submittedAt, ticket.category),
    department: DEPARTMENTS[ticket.category]?.name || 'General Administration',
    assignedTo: null, assignedName: null,
    comments: [],
    statusHistory: [{ from: null, to: 'Pending', by: ticket.studentName, at: submittedAt }],
    resolvedAt: null, escalatedAt: null, rating: null,
  };
  const res = await fetch(`${API}/tickets`, {
    method: 'POST', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify(newTicket)
  });
  return res.json();
};

