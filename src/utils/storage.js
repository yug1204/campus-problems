// ─── SLA Helpers ────────────────────────────────────────────────────────────
export const getSlaStatus = (ticket) => {
  if (ticket.status === 'Resolved' || ticket.status === 'Closed') return { status: 'done', pct: 100, label: 'Resolved', color: 'var(--secondary)' };
  if (!ticket.slaDeadline) return null;
  const now = Date.now();
  const start = new Date(ticket.timestamp).getTime();
  const end = new Date(ticket.slaDeadline).getTime();
  const elapsed = now - start;
  const total = end - start;
  const pct = Math.min(Math.round((elapsed / total) * 100), 100);
  const hoursLeft = Math.max(0, Math.round((end - now) / 3600000));
  if (now > end) return { status: 'breached', pct: 100, label: 'SLA Breached', color: 'var(--danger)', hoursLeft: 0 };
  if (pct >= 75) return { status: 'critical', pct, label: `${hoursLeft}h left`, color: 'var(--warning)', hoursLeft };
  return { status: 'ok', pct, label: `${hoursLeft}h left`, color: 'var(--secondary)', hoursLeft };
};

const makeSla = (submittedAt, category) => {
  const slaH = DEPARTMENTS[category]?.slaHours || 48;
  return new Date(new Date(submittedAt).getTime() + slaH * 3600000).toISOString();
};

