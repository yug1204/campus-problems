export const exportTicketsCSV = async () => {
  const tickets = await getTickets();
  const headers = ['ID', 'Title', 'Category', 'Priority', 'Status', 'Department', 'Student', 'Location', 'SLA Deadline', 'Submitted', 'Resolved At', 'Rating', 'Comments'];
  const rows = tickets.map(t => [
    t.id, `"${t.title}"`, t.category, t.priority, t.status, t.department || '',
    t.studentName || '', `"${t.location || ''}"`,
    t.slaDeadline ? new Date(t.slaDeadline).toLocaleString() : '',
    new Date(t.timestamp).toLocaleString(),
    t.resolvedAt ? new Date(t.resolvedAt).toLocaleString() : '',
    t.rating || '', t.comments?.length || 0,
  ]);
  const csv = [headers, ...rows].map(r => r.join(',')).join('\n');
  const blob = new Blob([csv], { type: 'text/csv' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url; a.download = `campus-solver-tickets-${new Date().toISOString().slice(0, 10)}.csv`;
  a.click(); URL.revokeObjectURL(url);
};

